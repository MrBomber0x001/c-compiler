#include <stdio.h>

/*
 * This is a multi-line comment
 * that spans multiple lines.
 */

int main() {
    int a = 5; // Another single-line comment
    float b = 3.14;
    char c = 'a';

    /* This is another multi-line comment */
    if ( a < b ) {
        return a + b;
    } else {
        return c;
    }
}